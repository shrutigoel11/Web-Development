let BaseURL ="https://cdn.jsdelivr.net/gh/fawazahmed0/currency-api@1/latest/currencies";
let dropdown = document.querySelectorAll(".dropdown select");
let button=document.querySelector("form button");
let msg=document.querySelector(".msg");
const from=document.querySelector(".from select");
const to=document.querySelector(".to select");
for (let select of dropdown) {
  for (let currenciescode in countryList) {
    let newOption = document.createElement("option");
    newOption.innerText = currenciescode;
    newOption.value = currenciescode;
    if (select.name === "from" && currenciescode === "USD") {
      newOption.selected = "selected";
    }
    if (select.name === "to" && currenciescode === "INR") {
      newOption.selected = "selected";
    }
    select.append(newOption);
  }

  console.log(select);
  select.addEventListener("change", (evt) => {
    console.log(select);
    updateFlag(evt.target);
  });
}

const updateFlag = (element) => {
  let currenciescode = element.value;
  // console.log(currenciescode);
  let countrycode = countryList[currenciescode];
  let newsrclink = `https://flagsapi.com/${countrycode}/shiny/64.png`;
  let img = element.parentElement.querySelector("img");
  img.src = newsrclink;
};


button.addEventListener("click",async (evt)=>{
    evt.preventDefault();
    let amount=document.querySelector(".amount input");
    let amtVal=amount.value;
    if(amtVal==" " || amtVal< 1){
        amtVal=1;
        amount.value="1";
    }
    const URL=`${BaseURL}/${from.value.toLowerCase()}/${to.value.toLowerCase()}.json`;
    let response=await fetch(URL);
    let data=response.json();
    let rate = data[to.value.toLowerCase()];

    let finalAmount = amtVal * rate;
    msg.innerText = `${amtVal} ${from.value} = ${finalAmount} ${to.value}`;
});

