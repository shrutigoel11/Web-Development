// Arithmetic operators:
// Binary operators
let a=5;
let b=7;
console.log("a+b =",a+b);
console.log("b-a =",b-a);
console.log("a*b =",a*b);
console.log("a/b =",a/b);
console.log("a^b =",a**b);

// Unary operator

let c=4;
let d=9;
console.log(++c);
console.log(d++);
console.log(c--);
console.log(--d);
// ++operand means first increase and operand++ means add afterwards;